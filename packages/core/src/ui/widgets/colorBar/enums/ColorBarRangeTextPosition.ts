/**
 * Specify the position of the text/ticks that are displayed when the user
 * interacts with the color bar. The position of the text depends on the
 * position of the color bar as follow:
 *   - TopOrLeft: horizontal/top OR vertical/left
 *   - BottomOrRight: horizontal/bottom OR vertical/right
 */
export enum ColorBarRangeTextPosition {
  TopOrLeft,
  BottomOrRight,
}
